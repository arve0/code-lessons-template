<section class="intro">
  <h1 class="intro" id="introduksjon">Introduksjon</h1>
  <p>
    Nå skal du endelig få lage ditt eget spill som du og vennene dine kan
    spille! Legg hodet i bløt og kom opp med en kul idé! Men før du går i gang
    med koding, må du legge en plan. Prøv også å dele opp spillet i flere mindre
    deler. Da ser du fort at hver del kan løses med sitt eget skript, eller ved
    å legg til noe på et annet. Tenk også tilbake på de andre spillene du har
    laget: Kan det være noen idéer derifra du kan bruke?
  </p>
</section>
<section class="activity">
  <h1 class="activity" id="steg-1%3A-planlegg-spillet">
    Steg 1: Planlegg spillet
  </h1>
  <p>
    Hva er formålet med spillet? Tenk for eksempel på Felix og Herbert. Der var
    målet å ikke bli tatt av katten. Det kan være smart å tenke ut flere
    spillidéer, og så velge den beste etterpå. Du kan gjerne drodle litt med en
    venn underveis.
  </p>
</section>
<section class="activity">
  <h1 class="activity" id="steg-2%3A-design-figurene">
    Steg 2: Design figurene
  </h1>
  <p>
    Hvilke figurer vil du ha med? Tegn de på et ark. Hvis de skal ha
    forskjellige drakter kan du skissere dem også. Spør gjerne om mer papir,
    hvis du trenger.
  </p>
</section>
<section class="activity">
  <h1 class="activity" id="steg-3%3A-design-scenen">Steg 3: Design scenen</h1>
  <p>
    Hvordan skal scenen se ut? Tegn den på et ark. Husk å tenke på hvordan
    karakterene skal forholde seg til den: for eksempel hvor de skal stå,
    hvordan de skal bevege seg, hvor mange det er plass til, etc.
  </p>
</section>
<section class="activity">
  <h1 class="activity" id="steg-4%3A-hvordan-virker-spillet-ditt%3F">
    Steg 4: Hvordan virker spillet ditt?
  </h1>
  <p>Tenk for eksempel på</p>
  <ul>
    <li>
      <p>[ ] Hvordan man vinner spillet?</p>
    </li>
    <li>
      <p>[ ] Hvordan man får poeng?</p>
    </li>
    <li>
      <p>[ ] Hvor mange spillere kan spille?</p>
    </li>
    <li>
      <p>[ ] Skal det være nedtelling?</p>
    </li>
    <li>
      <p>[ ] Hvordan styres spillet? Med tastene eller med museknappen?</p>
    </li>
    <li>
      <p>[ ] Skal det være lydeffekter? Hvilke?</p>
    </li>
  </ul>
  <p>Skriv ned tankene dine!</p>
</section>
<section class="activity">
  <h1 class="activity" id="steg-5%3A-sett-i-gang!">Steg 5: Sett i gang!</h1>
  <p>
    Nå som du har en plan, kan du begynne med utviklingen av figurene og
    skriptene dine.
  </p>
